package threeFour;

public class SwitchDurumu {
    public static void main(String[] args) {

        int kosul =4;

        switch (kosul){
            case 1:
                System.out.println("Durum 1");
                break;
            case 2:
                System.out.println("Durum 2");
                break;
            case 3:
                System.out.println("Durum 3");
                break;
            default:
                System.out.println("Default durum");
                break;
        }
    }
}
