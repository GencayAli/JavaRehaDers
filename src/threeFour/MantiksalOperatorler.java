package threeFour;

public class MantiksalOperatorler {
    public static void main(String[] args) {

        int a = 15, b =10;

        System.out.println(a == b && a > b); // false && true == false
        System.out.println(a == b || a > b); // false || true == true
        System.out.println(!(a == b && a > b)); // !(false && true == true








    }
}
